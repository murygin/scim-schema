package org.osiam.resources.scim;

import java.io.IOException;
import java.io.InputStream;
import java.net.MalformedURLException;
import java.net.URISyntaxException;
import java.net.URL;
import java.util.logging.Level;
import java.util.logging.Logger;

import org.osiam.resources.helper.DataUri;

import com.fasterxml.jackson.annotation.JsonProperty;

/**
 * This class represents a photo attribute.
 * 
 * <p>
 * For more detailed information please look at the <a
 * href="http://tools.ietf.org/html/draft-ietf-scim-core-schema-02#section-3.2">SCIM core schema 2.0, section 3.2</a>
 * </p>
 */
public class Photo extends MultiValuedAttribute {

    private static final Logger LOGGER = Logger.getLogger(Photo.class.getName());
    
    public static final String DATA_IMAGE = "data:image/";
    public static final String BASE64 = ";base64,";

    @JsonProperty
    private Type type;

    /**
     * Default constructor for Jackson
     */
    private Photo() {
    }

    private Photo(Builder builder) {
        super(builder);
        this.type = builder.type;
    }

    @Override
    public String getOperation() {
        return super.getOperation();
    }

    @Override
    public String getValue() {
        return super.getValue();
    }
    
    public URL getValueAsUrl() {
        try {
            return DataUri.convertStringToUrl(super.getValue());
        } catch (MalformedURLException e) {
            LOGGER.log(Level.SEVERE, "Error while converting string to URL.", e);
            throw new RuntimeException(e);
        }
    }

    @Override
    public String getDisplay() {
        return super.getDisplay();
    }

    @Override
    public boolean isPrimary() {
        return super.isPrimary();
    }

    /**
     * Gets the type of the attribute.
     * 
     * <p>
     * For more detailed information please look at the <a href=
     * "http://tools.ietf.org/html/draft-ietf-scim-core-schema-02#section-3.2" >SCIM core schema 2.0, section 3.2</a>
     * </p>
     * 
     * @return
     * 
     * @return the actual type
     */
    public Type getType() {
        return type;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = super.hashCode();
        result = prime * result + ((type == null) ? 0 : type.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (!super.equals(obj)) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        Photo other = (Photo) obj;
        if (type == null) {
            if (other.type != null) {
                return false;
            }
        } else if (!type.equals(other.type)) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "Photo [value=" + getValue() + ", type=" + type.toString() + ", primary=" + isPrimary() 
                + ", operation=" + getOperation() + "]";
    }

    /**
     * Builder class that is used to build {@link Photo} instances
     */
    public static class Builder extends MultiValuedAttribute.Builder {

        private Type type;

        public Builder() {
        }

        /**
         * builds an Builder based of the given Attribute
         * 
         * @param photo
         *        existing Attribute
         */
        public Builder(Photo photo) {
            super(photo);
            type = photo.type;
        }

        @Override
        public Builder setOperation(String operation) {
            super.setOperation(operation);
            return this;
        }

        @Override
        public Builder setDisplay(String display) {
            super.setDisplay(display);
            return this;

        }

        @Override
        public Builder setValue(String value) {
            super.setValue(value);
            return this;
        }
        
        public Builder setValue(URL url) {
            super.setValue(DataUri.convertUrlToString(url));
            return this;
        }

        public Builder setValue(InputStream is, String mimeType) {
            try {
                super.setValue(DataUri.convertIsToDataUriString(is, mimeType));
                return this;
            } catch (IOException ioe) {
                LOGGER.log(Level.SEVERE, "IO error while converting input stream to data uri.", ioe);
                throw new RuntimeException(ioe);
            } catch (URISyntaxException use) {
                LOGGER.log(Level.SEVERE, "URI syntax error while converting input stream to data uri.", use);
                throw new RuntimeException(use);
            }
        }

        /**
         * Sets the label indicating the attribute's function (See {@link MultiValuedAttribute#getType()}).
         * 
         * @param type
         *        the type of the attribute
         * @return the builder itself
         */
        public Builder setType(Type type) {
            this.type = type;
            return this;
        }

        @Override
        public Builder setPrimary(boolean primary) {
            super.setPrimary(primary);
            return this;
        }

        @Override
        public Photo build() {
            return new Photo(this);
        }
    }

    /**
     * Represents a photo type. Canonical values are available as static constants.
     */
    public static class Type extends MultiValuedAttributeType {
        public static final Type PHOTO = new Type("photo");
        public static final Type THUMBNAIL = new Type("thumbnail");

        public Type(String value) {
            super(value);
        }
    }

}
